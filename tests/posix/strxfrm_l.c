#define TEST(loc, a, b, val) do { \
	size_t len_a = strxfrm_l(NULL, a, 0, loc); \
	size_t len_b = strxfrm_l(NULL, b, 0, loc); \
	char *buf_a = malloc(len_a + 1); \
	char *buf_b = malloc(len_b + 1); \
	assert(buf_a && buf_b); \
	size_t ret_a = strxfrm_l(buf_a, a, len_a + 1, loc); \
	size_t ret_b = strxfrm_l(buf_b, b, len_b + 1, loc); \
	assert(ret_a == len_a); \
	assert(ret_b == len_b); \
	int cmp_result = strcmp(buf_a, buf_b); \
	assert(cmp_result val); \
	fprintf(stderr, "Comparing '%s' vs '%s': result %d\n", a, b, cmp_result); \
	fprintf(stderr, "char expect_a[] = {"); \
	for (size_t i = 0; i < ret_a; i++) \
		fprintf(stderr, "0x%02hhx, ", buf_a[i]); \
	fprintf(stderr, "};\n"); \
	fprintf(stderr, "char expect_b[] = {"); \
	for (size_t i = 0; i < ret_b; i++) \
		fprintf(stderr, "0x%02hhx, ", buf_b[i]); \
	fprintf(stderr, "};\n"); \
	free(buf_a); \
	free(buf_b); \
} while(0)

#define TEST_EXPECT(loc, a, b, val, res_a, res_b) do { \
