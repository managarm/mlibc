// glibc's wcscmp only returns -1, 0 or 1
static int wide_compare(const wchar_t *l, const wchar_t *r) {
	for(; *l == *r && *l && *r; l++, r++);
	return *l - *r;
}

#define TEST(loc, a, b, val) do { \
	size_t len_a = wcsxfrm_l(NULL, a, 0, loc); \
	size_t len_b = wcsxfrm_l(NULL, b, 0, loc); \
	wchar_t *buf_a = malloc(sizeof(wchar_t) * (len_a + 1)); \
	wchar_t *buf_b = malloc(sizeof(wchar_t) * (len_b + 1)); \
	assert(buf_a && buf_b); \
	size_t ret_a = wcsxfrm_l(buf_a, a, len_a + 1, loc); \
	size_t ret_b = wcsxfrm_l(buf_b, b, len_b + 1, loc); \
	assert(ret_a == len_a); \
	assert(ret_b == len_b); \
	int cmp_result = wide_compare(buf_a, buf_b); \
	assert(cmp_result val); \
	fprintf(stderr, "Comparing '%ls' vs '%ls': result %d\n", a, b, cmp_result); \
	fprintf(stderr, "wchar_t expect_a[] = {"); \
	for (size_t i = 0; i < ret_a; i++) \
		fprintf(stderr, "0x%x, ", buf_a[i]); \
	fprintf(stderr, "};\n"); \
	fprintf(stderr, "wchar_t expect_b[] = {"); \
	for (size_t i = 0; i < ret_b; i++) \
		fprintf(stderr, "0x%x, ", buf_b[i]); \
	fprintf(stderr, "};\n"); \
	free(buf_a); \
	free(buf_b); \
} while(0)

#define TEST_EXPECT(loc, a, b, val, res_a, res_b) do { \
	size_t len_a = wcsxfrm_l(NULL, a, 0, loc); \
	size_t len_b = wcsxfrm_l(NULL, b, 0, loc); \
	wchar_t *buf_a = malloc((len_a + 1) * sizeof(wchar_t)); \
	wchar_t *buf_b = malloc((len_b + 1) * sizeof(wchar_t)); \
	assert(buf_a && buf_b); \
	size_t ret_a = wcsxfrm_l(buf_a, a, len_a + 1, loc); \
	size_t ret_b = wcsxfrm_l(buf_b, b, len_b + 1, loc); \
	assert(ret_a == len_a); \
	assert(ret_b == len_b); \
	int cmp_result = wide_compare(buf_a, buf_b); \
	assert(cmp_result val); \
	fprintf(stderr, "Comparing '%ls' vs '%ls': result %d\n", a, b, cmp_result); \
	assert(len_a == (sizeof(res_a) / sizeof(wchar_t))); \
	assert(len_b == (sizeof(res_b) / sizeof(wchar_t))); \
	assert(!wcsncmp(buf_a, res_a, len_a)); \
	assert(!wcsncmp(buf_b, res_b, len_b)); \
	free(buf_a); \
	free(buf_b); \
} while(0)

